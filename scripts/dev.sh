#!/bin/bash
# Обертка для запуска dev сервера с правильными настройками хоста
# Игнорирует флаг --host если он передан без значения

# Удаляем --host из аргументов если он передан без значения
args=()
for arg in "$@"; do
  if [ "$arg" != "--host" ] && [ "$arg" != "-h" ]; then
    args+=("$arg")
  fi
done

# Запускаем с явным указанием хоста
npx nuxt dev --host=0.0.0.0 "${args[@]}"


